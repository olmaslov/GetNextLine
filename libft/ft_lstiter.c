#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst == NULL || f == NULL)
		return ;
	if (lst->next == NULL)
	{
		f(lst);
		return ;
	}
	f(lst);
	ft_lstiter(lst->next, f);
}
